		<h2>Persona</h2>
		<p><strong>Name:</strong> Sally Struthers </p>
		<p><strong>Age:</strong> 68</p>
		<p><strong>Profession:</strong> Retired actress and philanthropist, Sally Struthers is a 'beer aficionado.'</p>
		<p><strong>Technology:</strong> Sally travels a great deal both for pleasure and to make personal appearances in support of her charities. She gets a new iPhone with every new release, and utilizes various tablets as new products are introduced. </p>
		<p><strong>Attitudes and Behaviors:</strong> Sally enjoys using both online and downloadable apps for entertainment purposes. Because she is very social, Sally likes to find interesting places to eat, drink and meet locals in every place she visits. Sally often uses Yelp, but sometimes finds Yelp unreliable in terms of finding unique places to enjoy. Being a long time advocate of craft beers, Sally plans to explore her love of micro-brews during her stay in New Mexico. She wants to try the award winning beers, but also wants to go 'off the beaten path' and try lesser known brews. </p>

		<strong>Goals:</strong>
		<ul>
			<li>Sally would prefer to avoid discovering new beers by trial and error.</li>
			<li>Sally wants to find beers that are more malty than hoppy. </li>
			<li>Sally needs clear information on where to find these particular brews.</li>
			<li>Sally is very social and knowledgeable about craft brews. She would love to interact both on and offline with like-minded people. She also networks in this way.</li>
			<li>Sally is a very 'visual' person and needs a friendly user interface.</li>
			<li>Other than location, Sally would like to see the following information regarding both the brewery's and the individual brews:
				<ul>
					<li>
						Consumer rating of both the brewery and individual brews
					</li>
					<li>
						Beer descriptors such as style, IBU, and ABV
					</li>
					<li>
						Awards won 
					</li>
					<li>
						Information on brewery history, their brew master(s), and crafters
					</li>
				</ul>
			</li>
		</ul>




